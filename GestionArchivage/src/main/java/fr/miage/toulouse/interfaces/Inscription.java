    private void buttonInscriptionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonInscriptionActionPerformed
        if(isValidate()){
            nom = textFieldNom.getText();
            mail = textFieldMail.getText();
            password = textFieldPassword1.getText();
            adresse = textFieldAdresse.getText();
            this.roles = jCheckBoxDistributor.isSelected() ? this.roles + ";" + Constants.ROLES[1] : this.roles;
            
            UtilisateurDTO userDto = gestionArchive.inscription(nom, mail, password, adresse, roles);
            if(userDto != null){
                textFieldNom.setText("");
                textFieldMail.setText("");
                textFieldAdresse.setText("");
                textFieldPassword1.setText("");
                textFieldPassword2.setText("");
                jCheckBoxDistributor.setSelected(false);
                JOptionPane.showMessageDialog(this, Constants.SUCCES);
            }else{
                JOptionPane.showMessageDialog(this, Constants.FAILED, "Inane warning", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_buttonInscriptionActionPerformed

