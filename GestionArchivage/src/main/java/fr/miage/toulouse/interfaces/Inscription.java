    public boolean isValidate(){
        if(textFieldNom.getText().equals("")){
            JOptionPane.showMessageDialog(this, Constants.EMPTY_NAME, "Inane warning", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if(textFieldMail.getText().equals("")){
            JOptionPane.showMessageDialog(this, Constants.EMPTY_EMAIL, "Inane warning", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if(textFieldPassword1.getText().equals("")){
            JOptionPane.showMessageDialog(this, Constants.EMPTY_PASWWORD, "Inane warning", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if(!textFieldPassword2.getText().equals(textFieldPassword1.getText())){
            JOptionPane.showMessageDialog(this, Constants.PASSWORD_NOT_MATCH, "Inane warning", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if(textFieldAdresse.getText().equals("")){
            JOptionPane.showMessageDialog(this, Constants.EMPTY_ADRESSE, "Inane warning", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        return true;
    }
